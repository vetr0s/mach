// Create a font texture from embedded bitmap data.
Font* font_create(SDL_Renderer *rend) {
    if (!rend) return NULL;

    Font *font = (Font *)malloc(sizeof(Font));
    if (!font) return NULL;

    font->char_width = FONT_CHAR_WIDTH;
    font->char_height = FONT_CHAR_HEIGHT;

    // Create a surface for the font texture (16 chars wide × 8 rows = 128 ASCII chars)
    i32 texture_width = FONT_CHARS_PER_ROW * FONT_CHAR_WIDTH;
    i32 texture_height = FONT_NUM_ROWS * FONT_CHAR_HEIGHT;

    SDL_Surface *surface = SDL_CreateSurface(texture_width, texture_height, SDL_PIXELFORMAT_RGBA8888);
    if (!surface) {
        free(font);
        return NULL;
    }

    // Fill surface with font data
    u32 white = SDL_MapSurfaceRGB(surface, 255, 255, 255);
    u32 black = SDL_MapSurfaceRGB(surface, 0, 0, 0);

    const u8 *data = FONT_DATA;
    for (i32 char_idx = 0; char_idx < 96; char_idx++) {
        i32 char_x = (char_idx % FONT_CHARS_PER_ROW) * FONT_CHAR_WIDTH;
        i32 char_y = (char_idx / FONT_CHARS_PER_ROW) * FONT_CHAR_HEIGHT;

        for (i32 row = 0; row < FONT_CHAR_HEIGHT; row++) {
            u8 byte_val = data[char_idx * FONT_CHAR_HEIGHT + row];
            for (i32 col = 0; col < FONT_CHAR_WIDTH; col++) {
                u32 pixel = (byte_val & (0x80 >> col)) ? white : black;
                SDL_Rect rect = {char_x + col, char_y + row, 1, 1};
                SDL_FillSurfaceRect(surface, &rect, pixel);
            }
        }
    }

    font->texture = SDL_CreateTextureFromSurface(rend, surface);
    SDL_DestroySurface(surface);

    if (!font->texture) {
        free(font);
        return NULL;
    }

    SDL_SetTextureBlendMode(font->texture, SDL_BLENDMODE_BLEND);
    return font;
}

void font_destroy(Font *font) {
    if (!font) return;
    if (font->texture) {
        SDL_DestroyTexture(font->texture);
    }
    free(font);
}

void font_render_text(SDL_Renderer *rend, Font *font, const char *text, i32 x, i32 y, u8 r, u8 g, u8 b) {
    if (!rend || !font || !text) return;

    SDL_SetTextureColorMod(font->texture, r, g, b);

    i32 cur_x = x;
    for (const char *c = text; *c; c++) {
        u8 ch = (u8)*c;
        if (ch < 32 || ch > 127) continue;

        i32 char_idx = ch - 32;
        i32 src_x = (char_idx % FONT_CHARS_PER_ROW) * font->char_width;
        i32 src_y = (char_idx / FONT_CHARS_PER_ROW) * font->char_height;

        SDL_FRect src = {(f32)src_x, (f32)src_y, (f32)font->char_width, (f32)font->char_height};
        SDL_FRect dst = {(f32)cur_x, (f32)y, (f32)font->char_width, (f32)font->char_height};

        SDL_RenderTexture(rend, font->texture, &src, &dst);
        cur_x += font->char_width;
    }
}
